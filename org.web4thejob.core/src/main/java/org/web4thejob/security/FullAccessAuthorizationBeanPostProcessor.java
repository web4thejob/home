package org.web4thejob.security;

import org.springframework.beans.BeansException;

/**
 * <p>Implementation for applying full access to all {@link SecuredResource} instances. Ideal for environments where
 * access control is not needed or running tests.</p>
 *
 * @author Veniamin Isaias
 * @since 1.0.0
 */

public class FullAccessAuthorizationBeanPostProcessor implements AuthorizationBeanPostProcessor {

    @Override
    public Object postProcessBeforeInitialization(Object bean, String beanName) throws BeansException {
        return bean;
    }

    @Override
    public Object postProcessAfterInitialization(Object bean, String beanName) throws BeansException {
        return bean;
    }
}
